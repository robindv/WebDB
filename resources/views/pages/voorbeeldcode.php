<h2>Voorbeeldcode</h2>

<p>Om je opweg te helpen met het webprogrammeren is er voorbeeldcode beschikbaar.</p>
<p>Deze code kun je raadplegen op <?php echo link_to('http://'.env('WEBDB_EXAMPLES'),env('WEBDB_EXAMPLES')); ?>.
    Je kunt door de mappen en bestanden bladeren en de broncode bekijken.
    Het wachtwoord voor deze website kun je vinden in de syllabus.</p>
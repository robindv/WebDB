<h2>Inloggen mislukt</h2>

<p>Helaas is het inloggen mislukt, dit komt waarschijnlijk omdat je UvAnetID niet is opgenomen in onze database.
    <br />
    Indien je studentassistent bent kan dit komen omdat je nog met het verkeerde UvAnetID bent ingelogd, klik in dat geval
    <a href="https://auth.innovatievooronderwijs.nl/logout">hier</a> om uit te loggen.
    <br />
    Is dit niet het geval en moet je wel kunnen inloggen? Neem dan contact op met Robin de Vries (Robin punt deVries apenstaartje uva punt nl).
</p>
